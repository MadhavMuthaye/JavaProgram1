package JavaDay_16Feb_WrapperClass;

public class Ex1_WrapperClass {
    public static void main(String[] args) {
        int a = 10;  //int is data type
        Integer a1= 20;// Integer is wrapper class whose object can be created
        Integer b1  =30;// type of wrapper class
        System.out.println(Integer.max(a1,b1));
        System.out.println(Integer.sum(a1,b1));
    }



}
