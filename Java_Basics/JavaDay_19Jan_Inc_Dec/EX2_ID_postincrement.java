public class EX2_ID_postincrement {
    public static void main(String[] args) {
        //pre increament -- > a ++
        // print first and then increase
        int a=10; // value of a is 10


        System.out.println(a++);// print value of a i.e. 10 and then increment by  1
        System.out.println(a); //value is 11


    }
}
